package cateristicas;

public class ClassesWrapers {
  public static void main(String[] args) {
    Byte numero = 127;

    Short numero2 = 1233;

    Integer numero3 = 1237;

    Long numero4 = 1234567L;

    Float numero5 = 5.56F;

    Double numero6 = 6.65D;

    Boolean condicao = true;

    Character a = 'a';

    System.out.println("Byte -> " + numero);
    System.out.println("Short -> " + numero2);
    System.out.println("Integer -> " + numero3);
    System.out.println("Long -> " + numero4);
    System.out.println("Float -> " + numero5);
    System.out.println("Double -> " + numero6);
    System.out.println("Boolean -> " + condicao);
  }
}
